<p><strong>Artículo 6.</strong>El acoso escolar es una forma específica de la violencia escolar y que consiste en la intimidación o el maltrato entre escolares, de forma repetida y mantenida en el tiempo, con la intención de humillar y someter abusivamente a una víctima indefensa, a través de agresiones físicas, verbales, psicológicas y/o sociales.</p>
